    public function run()
    {
        $english_translations = $this->englishLanguageNames();
        $spanish_translations = $this->spanishLanguageNames();
        foreach ($english_translations as $locale => $name) {
            if (!Str::contains($locale, '_') && Str::limit($locale, 3) == $locale) {
                if (in_array($locale, ['es', 'en'])) {
                    $active = 1;
                } else {
                    $active = 0;
                }
                $new_language = Language::create([
                    'locale' => $locale,
                    'active' => $active
                ]);
                LanguageTranslation::create([
                    'language_id' => $new_language->id,
                    'locale' => 'en',
                    'name' => ucfirst($name)
                ]);
                LanguageTranslation::create([
                    'language_id' => $new_language->id,
                    'locale' => 'es',
                    'name' => ucfirst($spanish_translations[$new_language->locale])
                ]);
            }
        }
    }

